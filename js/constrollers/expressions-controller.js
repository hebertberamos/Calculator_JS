export function sum(n1, n2){
    const num1 = parseFloat(n1);
    const num2 = parseFloat(n2);
    const result = num1 + num2;
    return result;
}

export function subtract(n1, n2){
    const num1 = parseFloat(n1);
    const num2 = parseFloat(n2);
    const result = num1 - num2;
    return(result);
}

export function multiply(n1, n2){
    const num1 = parseFloat(n1);
    const num2 = parseFloat(n2);
    const result = num1 * num2;
    return(result);
}

export function division(n1, n2){
    const num1 = parseFloat(n1);
    const num2 = parseFloat(n2);
    const result = num1 / num2;
    return(result);
}
